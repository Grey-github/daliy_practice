#使用一个字典来存储一个熟人的信息，
#包括名、姓、年龄和居住的城市。
#该字典应包含键first_name 、last_name 、age 和city 。
#将存储在该字典中的每项信息都打印出来。
familiar_person = {
	'first_name': 'Zhi', 
	'last_name': 'Li', 
	'age': 19, 
	'city': 'Guangzhou'
	}
print(familiar_person['first_name'])
print(familiar_person['last_name'])
print(familiar_person['age'])
print(familiar_person['city'])

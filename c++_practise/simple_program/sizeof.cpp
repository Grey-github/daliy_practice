# include <iostream>
using namespace std;
int main()
{
	cout<<"char 所占字节数为："<<sizeof(char)<<endl;
	cout<<"short int 所占字节数为："<<sizeof(short int)<<endl; 
	cout<<"int 所占字节数为："<<sizeof(int)<<endl;
	cout<<"long int 所占字节数为："<<sizeof(long int)<<endl;
	cout<<"long double 所占字节数为："<<sizeof(long double)<<endl;
	cout<<"double 所占字节数为："<<sizeof(double)<<endl;
}

